////////////////////////////////////////////////////////////////////////////////
// INCLUDES
////////////////////////////////////////////////////////////////////////////////
#include "sprite.h"
#include "opengl.h"

////////////////////////////////////////////////////////////////////////////////
// PRIVATE DEFINITIONS
////////////////////////////////////////////////////////////////////////////////
#define HERO_WIDTH 20
#define HERO_HEIGHT 20
#define HERO_SPEED 5

////////////////////////////////////////////////////////////////////////////////
// PRIVATE VARIABLES
////////////////////////////////////////////////////////////////////////////////
static sprite *hero;
static int hero_x = 0, hero_y = 0;

////////////////////////////////////////////////////////////////////////////////
// PUBLIC FUNCTIONS
////////////////////////////////////////////////////////////////////////////////
void char_create() {
  hero = sprite_create(HERO_WIDTH, HERO_HEIGHT, 0x00000080);
}

void char_render() {
  opengl_draw_sprite(hero_x, hero_y, hero);
}

void char_delete() {
  sprite_delete(hero);
}

void char_key_press(unsigned char key, int x, int y) {
  if (key == 'w') {
    hero_y -= 1 * HERO_SPEED;
  }
  if (key == 'a') {
    hero_x -= 1 * HERO_SPEED;
  }
  if (key == 's') {
    hero_y += 1 * HERO_SPEED;
  }
  if (key == 'd') {
    hero_x += 1 * HERO_SPEED;
  }
}
